package easy;



public class Exercicio05 {// inicio classe
    public int somaDeTresNumeros(int primeiroNumero, int segundoNumero, int terceiroNumero) {
        int soma = primeiroNumero + segundoNumero + terceiroNumero;
        return soma;
    }

    public int subtracaoDeTresNumeros(int primeiroNumero, int segundoNumero, int terceiroNumero) {
        return primeiroNumero - segundoNumero - terceiroNumero;
    }

    public int multiplicacaoDeTresNumeros(int primeiroNumero, int segundoNumero, int terceiroNumero) {
        return primeiroNumero * segundoNumero * terceiroNumero;
    }

    public int mediaDeTresNumeros(int primeiroNumero, int segundoNumero, int terceiroNumero) {
        return primeiroNumero % segundoNumero % terceiroNumero;
    }

}

